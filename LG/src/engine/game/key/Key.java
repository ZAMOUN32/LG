package engine.game.key;

/**
 * The Key abstract class represents a generic keyboard input handler.
 * Specific implementations should extend this class.
 */
public abstract class Key {

	public boolean upPressed, downPressed, leftPressed, rightPressed;
	public boolean ePressed, aPressed,fPressed, rPressed;
	public boolean spacePressed;
	public boolean shiftPressed, controlPressed;
	
}

